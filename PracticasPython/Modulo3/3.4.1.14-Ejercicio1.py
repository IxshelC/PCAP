lst = [1, 2, 3, 4, 5]   # Lista
lst.insert(1, 6)        # Inserta en la posición 1 el número 6 , que es donde está el número 2
del lst[0]              # Elimina la posición 0 , es el número "1"
lst.append(1)           # Agrega en la última posición el 1

print(lst)              # Imprime la lista

