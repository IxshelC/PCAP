# LISTA ORIGINAL

numbers = [0, 1, 2, 3]  # Lista
print(len(numbers))     # Conteo de la lista
print(numbers)          # Imprime lista

### Método APPEND

numbers.append(4)       # Agrega un número al final de la lista "4"
print(len(numbers))
print(numbers)

### Método INSERT
numbers.insert(0, -1)   # El primer elemento es la posición "0", el segundo el valor que se va insertar "-1"
print(len(numbers))
print(numbers)
